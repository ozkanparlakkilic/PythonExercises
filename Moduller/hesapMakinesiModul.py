
def toplama(sayi1,sayi2):
    """Toplama islemi"""
    return sayi1 + sayi2

def cikarma(sayi1,sayi2):
    """Cikarma islemi"""
    return sayi1 - sayi2

def carpma(sayi1,sayi2):
    """ Carpma islemi"""
    return sayi1 * sayi2

def bolme(sayi1,sayi2):
    """Bolme islemi"""
    return sayi1 / sayi2

def modAlma(sayi1,sayi2):
    """Mod Alma islemi"""
    return sayi1 % sayi2

def usAlma(sayi1,sayi2):
    """Us Alma islemi"""
    return sayi1 ** sayi2

def kokAlma(sayi1,sayi2):
    """Kok Alma islemi"""
    return sayi1 ** (1/sayi2)


